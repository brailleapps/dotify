package org.daisy.dotify.translator;

public class UnsupportedSpecificationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1834392372293939932L;

	public UnsupportedSpecificationException() {
		super();
	}

	public UnsupportedSpecificationException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnsupportedSpecificationException(String message) {
		super(message);
	}

	public UnsupportedSpecificationException(Throwable cause) {
		super(cause);
	}

}
